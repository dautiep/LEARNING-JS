/** 
 * 3. Toán tử so sánh - Comparision
 *      3.1. ==      --> bằng
 *      3.2. !=      --> khác (không bằng)
 *      3.3. >       --> lớn hơn
 *      3.4. <       --> nhỏ hơn
 *      3.5. >=      --> lớn hơn hoặc bằng
 *      3.6. <=      --> nhỏ hơn hoặc bằng
 *      
*/

console.log('--------------Toán tử so sánh--------------');

var a = 1;
var b = 2;

//Toán tử ==
if (a == b) {
    console.log('a == b : Dieu kien dung');
} else {
    console.log('a == b : Dieu kien sai');
}

//Toán tử !=
if (a != b) {
    console.log('a != b : Dieu kien dung');
} else {
    console.log('a != b : Dieu kien sai');
}

//Toán tử >
if (a > b) {
    console.log('a > b : Dieu kien dung');
} else {
    console.log('a > b : Dieu kien sai');
}

//Toán tử <
if (a < b) {
    console.log('a < b : Dieu kien dung');
} else {
    console.log('a < b : Dieu kien sai');
}

//Toán tử >=
if (a >= b) {
    console.log('a >= b : Dieu kien dung');
} else {
    console.log('a >= b : Dieu kien sai');
}

//Toán tử <=
if (a <= b) {
    console.log('a <= b : Dieu kien dung');
} else {
    console.log('a <= b : Dieu kien sai');
}